#include <iostream>
using namespace std;
    
int main()
{
    int a, b, c;
    cout << "Please enter 3 integer numbers:\n";
    cout << "a = "; cin >> a;
    cout << "b = "; cin >> b;
    cout << "c = "; cin >> c;
    cout << "The average of 3 integers number: " << (a + b + c) / 3 << endl;
    return 0;
}